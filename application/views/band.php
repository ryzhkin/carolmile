         <p class="cm-band-content-text">
             Группа »Carol Mile« была основана в Мюнхене в 2011 году.
             Интересна история возникновения названия группы »Carol Mile«: »Carol« в переводе с английского ознчает »веселая песнь« или же »гимн« (обычно - Рождество). »Mile« – это миля, так что в переводе название группы может звучать как »весёлая миля«. В украинском языке есть аналог слова »carol« – »коляда«, т.е. род особой народной песни, которую поют в предрождественский период и на Рождество. Один из проектов ансамбля – это исполнение Колядок в современных оранжировках. Кроме этого, группа гастолирует по городам Германии, сопровождает свадьбы, дни Рождения, Рождество и Новый год, а также выступает на корпоративах.
