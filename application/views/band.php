        <p class="cm-band-content-text">
            Die Band »Carol Mile« wurde im Jahr 2011 in München gegründet. Ihr Name kommt aus dem Englischen und bedeutet »fröhliches Meilenlied«. Dabei ist nur »Carol« als fröhliches Lied oder auch Hymne zu verstehen und »Mile« dagegen wird direkt als Meile wiedergegeben. Im Ukrainischen gibt es einen Analog des Wortes »Christmas Carol« (ukr.: Koljada), es ist eine Art von einem Volkslied, welches in der vorweihnachtlichen Zeit oder an Weihnachten gespielt wird. Eines der Projekte von dem Ensemble ist es Weihnachtslieder in einer moderneren Variante vorzuspielen. Außerdem tourt die Band innerhalb Deutschlands in verschiedenen Städten, tritt auf Hochzeiten, Geburtstagen, Weihnachten, Silvester und an Korporativen auf.
        </p>
